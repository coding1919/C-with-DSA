#include <iostream>
using namespace std;

int main() {
    // printing integer - 4byte

    // variable declaration
    int a;

    // variable defination
    a = 10;

    // together
    // int a = 10;

    // printing a
    cout<<a<<endl; 

    // many variable declaration and defination
    int b=10, c=20, d=30;

    // operation on b and c
    cout<<b+c<<endl;

    // - single line comment

    // printing charater - 1byte
    char chr = 'a';
    cout<<chr<<endl;

    // printing float - 4byte
    float f = 2.8f;
    cout<<f<<endl;

    // printing double - 8byte
    double db = 2.98363;
    cout<<db<<endl;

    // printing boolean - 1byte
    bool boolt = true; // 1
    cout<<boolt<<endl;
    bool boolf = false; // 0
    cout<<boolf<<endl;
    return 0;
}